interface I18nCtx { lang: Lang; t: (key: string) => string; toggleLang: () => void; }
const I18nContext = createContext<I18nCtx>({ lang: "ru", t: () => "", toggleLang: () => {} });

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(() => (localStorage.getItem("lang") as Lang) || "ru");
  const t = useCallback((key: string) => dict[lang][key] || key, [lang]);
  const toggleLang = () => { const n = lang === "ru" ? "en" : "ru"; localStorage.setItem("lang", n); setLang(n); };
  return <I18nContext.Provider value={{ lang, t, toggleLang }}>{children}</I18nContext.Provider>;
}

export function useT() { return useContext(I18nContext); }
